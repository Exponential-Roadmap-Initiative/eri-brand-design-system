/**
 * scripts/gen-eri-version.mjs
 *
 * Reads version constants from their canonical sources and writes them into
 * shared/eriVersion.ts as typed constants. This file is imported by client-side
 * React components so they always show the correct version without any hardcoding.
 *
 * Sources:
 *   ERI_COMPONENTS_VERSION  — packages/eri-components/package.json  (version field)
 *   ERI_BDS_SKILL_VERSION   — server/_core/index.ts                 (version string in /api/skill/latest.json response)
 *
 * Run automatically via the "prebuild" and "predev" npm scripts so the file is
 * always up to date before the client or server bundles are built.
 *
 * shared/eriVersion.ts is gitignored — never edit it manually.
 * To update ERI_BDS_SKILL_VERSION, change the version string in server/_core/index.ts
 * and then run `pnpm gen:version` (or restart the dev server, which runs predev).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

// ── ERI_COMPONENTS_VERSION ────────────────────────────────────────────────────
const pkg = JSON.parse(
  fs.readFileSync(path.join(root, "packages", "eri-components", "package.json"), "utf-8")
);
const componentsVersion = `v${pkg.version}`;

// ── ERI_BDS_SKILL_VERSION ─────────────────────────────────────────────────────
// Canonical source: the version string in the /api/skill/latest.json JSON response
// inside server/_core/index.ts.  We extract it with a regex rather than importing
// the TypeScript file so this script stays a plain Node.js ESM module.
const indexTs = fs.readFileSync(path.join(root, "server", "_core", "index.ts"), "utf-8");
// Matches: res.json({ url: SKILL_LATEST_URL, version: "3.3.0" })
const skillVersionMatch = indexTs.match(/res\.json\(\{[^}]*version:\s*"([^"]+)"/);
if (!skillVersionMatch) {
  throw new Error(
    "[gen-eri-version] Could not find skill version in server/_core/index.ts. " +
    "Expected a line like: res.json({ url: SKILL_LATEST_URL, version: \"3.x.x\" })"
  );
}
const skillVersion = `v${skillVersionMatch[1]}`;

// ── Write shared/eriVersion.ts ────────────────────────────────────────────────
const output = `// AUTO-GENERATED — do not edit manually.
// Re-generated by scripts/gen-eri-version.mjs via the predev/prebuild npm hooks.
// To update: change the source value, then run \`pnpm gen:version\` or restart the dev server.
//
// Sources:
//   ERI_COMPONENTS_VERSION  \u2190 packages/eri-components/package.json
//   ERI_BDS_SKILL_VERSION   \u2190 server/_core/index.ts  (/api/skill/latest.json version field)

export const ERI_COMPONENTS_VERSION = "${componentsVersion}" as const;
export const ERI_BDS_SKILL_VERSION = "${skillVersion}" as const;
`;

const outPath = path.join(root, "shared", "eriVersion.ts");
fs.writeFileSync(outPath, output, "utf-8");
console.log(`[gen-eri-version] wrote ${outPath}`);
console.log(`  ERI_COMPONENTS_VERSION = ${componentsVersion}`);
console.log(`  ERI_BDS_SKILL_VERSION  = ${skillVersion}`);
